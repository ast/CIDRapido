import Foundation
import UIKit

extension UIColor {
    class func hospitalGreen() -> UIColor {
        return UIColor(rgba: "#1fa67a")
    }
    
    class func shadowColor() -> UIColor {
        return UIColor(red: 0, green: 0, blue: 0, alpha: 0.15)
    }
    
    class func lightHospitalGreen() -> UIColor {
        return UIColor(rgba: "#62C1A2")
    }
    
}